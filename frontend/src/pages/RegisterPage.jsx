import { useState } from 'react';
import { Link, useNavigate } from 'react-router-dom';
import { motion } from 'framer-motion';
import { Mail, Lock, Eye, EyeOff, ArrowRight, Rocket, User, Briefcase } from 'lucide-react';
import { useDispatch, useSelector } from 'react-redux';
import { registerUser } from '../store/authSlice';

const RegisterPage = () => {
    const [showPassword, setShowPassword] = useState(false);
    const [formData, setFormData] = useState({
        name: '',
        email: '',
        password: '',
        role: 'freelancer'
    });

    const dispatch = useDispatch();
    const navigate = useNavigate();
    const { loading, error } = useSelector((state) => state.auth);

    const handleChange = (e) => {
        setFormData({
            ...formData,
            [e.target.name]: e.target.value
        });
    };

    const handleSubmit = async (e) => {
        e.preventDefault();
        const result = await dispatch(registerUser(formData));
        if (registerUser.fulfilled.match(result)) {
            navigate('/dashboard');
        }
    };

    const inputWrapperStyle = {
        position: 'relative',
        width: '100%'
    };

    const iconStyle = {
        position: 'absolute',
        left: '1rem',
        top: '50%',
        transform: 'translateY(-50%)',
        width: '1.25rem',
        height: '1.25rem',
        color: '#9ca3af',
        pointerEvents: 'none'
    };

    const inputStyle = {
        width: '100%',
        padding: '0.875rem 1rem 0.875rem 3rem',
        border: '2px solid #e5e5e5',
        borderRadius: '0.75rem',
        fontSize: '1rem',
        transition: 'all 0.2s ease',
        backgroundColor: 'white',
        outline: 'none'
    };

    const passwordInputStyle = {
        ...inputStyle,
        paddingRight: '3rem'
    };

    return (
        <div style={{
            minHeight: 'calc(100vh - 6rem)',
            display: 'flex',
            alignItems: 'center',
            justifyContent: 'center',
            padding: '3rem 1.5rem'
        }}>
            <motion.div
                initial={{ opacity: 0, y: 20 }}
                animate={{ opacity: 1, y: 0 }}
                transition={{ duration: 0.5 }}
                style={{ width: '100%', maxWidth: '28rem' }}
            >
                {/* Logo */}
                <div style={{ textAlign: 'center', marginBottom: '2rem' }}>
                    <Link to="/" style={{ display: 'inline-flex', alignItems: 'center', gap: '0.5rem', textDecoration: 'none' }}>
                        <div style={{
                            width: '3rem',
                            height: '3rem',
                            backgroundColor: 'var(--color-brand-orange)',
                            borderRadius: '0.75rem',
                            display: 'flex',
                            alignItems: 'center',
                            justifyContent: 'center'
                        }}>
                            <Rocket style={{ width: '1.5rem', height: '1.5rem', color: 'white' }} />
                        </div>
                    </Link>
                    <h1 className="font-heading" style={{ marginTop: '1.5rem', fontSize: '1.875rem', fontWeight: 700, color: 'var(--color-brand-black)' }}>
                        Create an account
                    </h1>
                    <p style={{ marginTop: '0.5rem', color: '#52525b' }}>
                        Join GigFlow and start your journey
                    </p>
                </div>

                {/* Register Card */}
                <div className="card">
                    <form onSubmit={handleSubmit} style={{ display: 'flex', flexDirection: 'column', gap: '1.25rem' }}>
                        {/* Error Message */}
                        {error && (
                            <motion.div
                                initial={{ opacity: 0, y: -10 }}
                                animate={{ opacity: 1, y: 0 }}
                                style={{
                                    backgroundColor: '#fef2f2',
                                    color: '#dc2626',
                                    padding: '0.75rem 1rem',
                                    borderRadius: '0.75rem',
                                    fontSize: '0.875rem'
                                }}
                            >
                                {error}
                            </motion.div>
                        )}

                        {/* Name Field */}
                        <div>
                            <label style={{ display: 'block', fontWeight: 500, marginBottom: '0.5rem', color: 'var(--color-brand-black)' }}>
                                Full Name
                            </label>
                            <div style={inputWrapperStyle}>
                                <User style={iconStyle} />
                                <input
                                    type="text"
                                    id="name"
                                    name="name"
                                    value={formData.name}
                                    onChange={handleChange}
                                    placeholder="John Doe"
                                    style={inputStyle}
                                    required
                                />
                            </div>
                        </div>

                        {/* Email Field */}
                        <div>
                            <label style={{ display: 'block', fontWeight: 500, marginBottom: '0.5rem', color: 'var(--color-brand-black)' }}>
                                Email Address
                            </label>
                            <div style={inputWrapperStyle}>
                                <Mail style={iconStyle} />
                                <input
                                    type="email"
                                    id="email"
                                    name="email"
                                    value={formData.email}
                                    onChange={handleChange}
                                    placeholder="you@example.com"
                                    style={inputStyle}
                                    required
                                />
                            </div>
                        </div>

                        {/* Password Field */}
                        <div>
                            <label style={{ display: 'block', fontWeight: 500, marginBottom: '0.5rem', color: 'var(--color-brand-black)' }}>
                                Password
                            </label>
                            <div style={inputWrapperStyle}>
                                <Lock style={iconStyle} />
                                <input
                                    type={showPassword ? 'text' : 'password'}
                                    id="password"
                                    name="password"
                                    value={formData.password}
                                    onChange={handleChange}
                                    placeholder="Create a strong password"
                                    style={passwordInputStyle}
                                    required
                                    minLength={6}
                                />
                                <button
                                    type="button"
                                    onClick={() => setShowPassword(!showPassword)}
                                    style={{
                                        position: 'absolute',
                                        right: '1rem',
                                        top: '50%',
                                        transform: 'translateY(-50%)',
                                        background: 'none',
                                        border: 'none',
                                        cursor: 'pointer',
                                        color: '#9ca3af',
                                        padding: 0,
                                        display: 'flex',
                                        alignItems: 'center'
                                    }}
                                >
                                    {showPassword ?
                                        <EyeOff style={{ width: '1.25rem', height: '1.25rem' }} /> :
                                        <Eye style={{ width: '1.25rem', height: '1.25rem' }} />
                                    }
                                </button>
                            </div>
                        </div>

                        {/* Role Selection */}
                        <div>
                            <label style={{ display: 'block', fontWeight: 500, marginBottom: '0.5rem', color: 'var(--color-brand-black)' }}>
                                I want to
                            </label>
                            <div style={{ display: 'grid', gridTemplateColumns: '1fr 1fr', gap: '0.75rem' }}>
                                <motion.button
                                    type="button"
                                    whileHover={{ scale: 1.02 }}
                                    whileTap={{ scale: 0.98 }}
                                    onClick={() => setFormData({ ...formData, role: 'freelancer' })}
                                    style={{
                                        padding: '1rem',
                                        borderRadius: '0.75rem',
                                        border: `2px solid ${formData.role === 'freelancer' ? 'var(--color-brand-orange)' : '#e5e5e5'}`,
                                        backgroundColor: formData.role === 'freelancer' ? 'rgba(255, 107, 74, 0.05)' : 'white',
                                        cursor: 'pointer',
                                        display: 'flex',
                                        flexDirection: 'column',
                                        alignItems: 'center',
                                        gap: '0.5rem',
                                        transition: 'all 0.2s'
                                    }}
                                >
                                    <User style={{
                                        width: '1.5rem',
                                        height: '1.5rem',
                                        color: formData.role === 'freelancer' ? 'var(--color-brand-orange)' : '#9ca3af'
                                    }} />
                                    <span style={{
                                        fontSize: '0.875rem',
                                        fontWeight: 500,
                                        color: formData.role === 'freelancer' ? 'var(--color-brand-orange)' : '#52525b'
                                    }}>
                                        Find Work
                                    </span>
                                </motion.button>

                                <motion.button
                                    type="button"
                                    whileHover={{ scale: 1.02 }}
                                    whileTap={{ scale: 0.98 }}
                                    onClick={() => setFormData({ ...formData, role: 'client' })}
                                    style={{
                                        padding: '1rem',
                                        borderRadius: '0.75rem',
                                        border: `2px solid ${formData.role === 'client' ? 'var(--color-brand-orange)' : '#e5e5e5'}`,
                                        backgroundColor: formData.role === 'client' ? 'rgba(255, 107, 74, 0.05)' : 'white',
                                        cursor: 'pointer',
                                        display: 'flex',
                                        flexDirection: 'column',
                                        alignItems: 'center',
                                        gap: '0.5rem',
                                        transition: 'all 0.2s'
                                    }}
                                >
                                    <Briefcase style={{
                                        width: '1.5rem',
                                        height: '1.5rem',
                                        color: formData.role === 'client' ? 'var(--color-brand-orange)' : '#9ca3af'
                                    }} />
                                    <span style={{
                                        fontSize: '0.875rem',
                                        fontWeight: 500,
                                        color: formData.role === 'client' ? 'var(--color-brand-orange)' : '#52525b'
                                    }}>
                                        Hire Talent
                                    </span>
                                </motion.button>
                            </div>
                        </div>

                        {/* Terms */}
                        <p style={{ fontSize: '0.75rem', color: '#71717a' }}>
                            By creating an account, you agree to our{' '}
                            <a href="#" style={{ color: 'var(--color-brand-orange)', textDecoration: 'none' }}>Terms of Service</a>
                            {' '}and{' '}
                            <a href="#" style={{ color: 'var(--color-brand-orange)', textDecoration: 'none' }}>Privacy Policy</a>
                        </p>

                        {/* Submit Button */}
                        <motion.button
                            type="submit"
                            whileHover={{ scale: 1.02 }}
                            whileTap={{ scale: 0.98 }}
                            disabled={loading}
                            className="btn-primary"
                            style={{
                                width: '100%',
                                display: 'flex',
                                alignItems: 'center',
                                justifyContent: 'center',
                                gap: '0.5rem',
                                opacity: loading ? 0.5 : 1,
                                cursor: loading ? 'not-allowed' : 'pointer'
                            }}
                        >
                            {loading ? (
                                <div style={{
                                    width: '1.25rem',
                                    height: '1.25rem',
                                    border: '2px solid white',
                                    borderTopColor: 'transparent',
                                    borderRadius: '50%',
                                    animation: 'spin 1s linear infinite'
                                }} />
                            ) : (
                                <>
                                    Create Account
                                    <ArrowRight style={{ width: '1.25rem', height: '1.25rem' }} />
                                </>
                            )}
                        </motion.button>
                    </form>

                    {/* Divider */}
                    <div style={{ position: 'relative', margin: '1.5rem 0' }}>
                        <div style={{ position: 'absolute', inset: 0, display: 'flex', alignItems: 'center' }}>
                            <div style={{ width: '100%', borderTop: '1px solid #e5e5e5' }} />
                        </div>
                        <div style={{ position: 'relative', display: 'flex', justifyContent: 'center' }}>
                            <span style={{ backgroundColor: 'white', padding: '0 1rem', fontSize: '0.875rem', color: '#71717a' }}>
                                or continue with
                            </span>
                        </div>
                    </div>

                    {/* Social Login */}
                    <div style={{ display: 'grid', gridTemplateColumns: '1fr 1fr', gap: '1rem' }}>
                        <button
                            className="btn-secondary"
                            style={{
                                fontSize: '0.875rem',
                                padding: '0.75rem',
                                display: 'flex',
                                alignItems: 'center',
                                justifyContent: 'center',
                                gap: '0.5rem'
                            }}
                        >
                            <svg style={{ width: '1.25rem', height: '1.25rem' }} viewBox="0 0 24 24">
                                <path fill="currentColor" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z" />
                                <path fill="currentColor" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" />
                                <path fill="currentColor" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z" />
                                <path fill="currentColor" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" />
                            </svg>
                            Google
                        </button>
                        <button
                            className="btn-secondary"
                            style={{
                                fontSize: '0.875rem',
                                padding: '0.75rem',
                                display: 'flex',
                                alignItems: 'center',
                                justifyContent: 'center',
                                gap: '0.5rem'
                            }}
                        >
                            <svg style={{ width: '1.25rem', height: '1.25rem' }} fill="currentColor" viewBox="0 0 24 24">
                                <path d="M12 2C6.477 2 2 6.477 2 12c0 4.42 2.865 8.164 6.839 9.489.5.092.682-.217.682-.482 0-.237-.009-.866-.013-1.7-2.782.603-3.369-1.342-3.369-1.342-.454-1.155-1.11-1.462-1.11-1.462-.908-.62.069-.608.069-.608 1.003.07 1.531 1.03 1.531 1.03.892 1.529 2.341 1.087 2.91.831.092-.646.35-1.086.636-1.336-2.22-.253-4.555-1.11-4.555-4.943 0-1.091.39-1.984 1.029-2.683-.103-.253-.446-1.27.098-2.647 0 0 .84-.269 2.75 1.025A9.578 9.578 0 0112 6.836c.85.004 1.705.114 2.504.336 1.909-1.294 2.747-1.025 2.747-1.025.546 1.377.203 2.394.1 2.647.64.699 1.028 1.592 1.028 2.683 0 3.842-2.339 4.687-4.566 4.935.359.309.678.919.678 1.852 0 1.336-.012 2.415-.012 2.743 0 .267.18.578.688.48C19.138 20.161 22 16.418 22 12c0-5.523-4.477-10-10-10z" />
                            </svg>
                            GitHub
                        </button>
                    </div>
                </div>

                {/* Login Link */}
                <p style={{ textAlign: 'center', marginTop: '1.5rem', color: '#52525b' }}>
                    Already have an account?{' '}
                    <Link to="/login" style={{ color: 'var(--color-brand-orange)', fontWeight: 600, textDecoration: 'none' }}>
                        Sign in
                    </Link>
                </p>
            </motion.div>

            {/* Keyframe animation for spinner */}
            <style>{`
        @keyframes spin {
          to { transform: rotate(360deg); }
        }
      `}</style>
        </div>
    );
};

export default RegisterPage;
